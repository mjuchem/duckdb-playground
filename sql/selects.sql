SELECT count() FROM menu_item_denorm;

SELECT
    EXTRACT(YEAR FROM menu_date::DATE) AS d,
    count(),
    round(avg(price), 2),
    bar(avg(price), 0, 100, 100)
FROM menu_item_denorm
WHERE (menu_currency = 'Dollars') AND (d > 0) AND (d < 2022)
GROUP BY d
ORDER BY d ASC;

